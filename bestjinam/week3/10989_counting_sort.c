#include <stdio.h>

int	main(void)
{
	int		n;
	int		num[10001] = {0,};
	int		a;

	scanf("%d", &n);
	for (int i = 0 ; i < n ; i++)
	{
		scanf("%d", &a);
		num[a]++;
	}
	for (int i = 1 ; i < 10001 ; i++)
	{
		while (num[i])
		{
			printf("%d\n", i);
			num[i]--;
		}
	}
}
